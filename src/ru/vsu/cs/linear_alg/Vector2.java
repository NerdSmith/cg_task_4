package ru.vsu.cs.linear_alg;

public class Vector2 {
    private final float first;
    private final float second;

    public Vector2(float first, float second) {
        this.first = first;
        this.second = second;
    }

    public float getFirst() {
        return first;
    }

    public float getSecond() {
        return second;
    }
}
